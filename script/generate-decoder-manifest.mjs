#!/usr/bin/env node
import { access, readFile, writeFile, mkdir } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const manifest = JSON.parse(await readFile(resolve(root, 'decoder-manifest.json'), 'utf8'));
const symbol = {openmpt:'decoder_openmpt',gme:'decoder_gme',sidplayfp:'decoder_sidplayfp',sc68:'decoder_sc68',ym6:'decoder_ym6',asap:'decoder_asap',ahx2play:'decoder_uade_ahx',midi:'decoder_midi',v2m:'decoder_v2m',soundmon:'decoder_bp',adplug:'decoder_adplug'};
const quote = value => JSON.stringify(value);
const c = manifest.formats.map(format => {
  const caps = format.seek || format.subsong ? 'NAV_CAPS' : 'BASE_CAPS';
  if (!symbol[format.decoder]) throw new Error(`No C decoder symbol for ${format.decoder}`);
  return `ENTRY(${quote(format.id)}, ${quote(format.name)}, ${quote(format.decoder)}, ${quote(format.version)}, ${quote(format.category)}, ${caps}, ${symbol[format.decoder]})`;
}).join(',\n') + '\n';
const output = resolve(root, 'Sources/OrzAudioKitCXX/dispatch/decoder_manifest.generated.inc');
const buildInfo = `#define ORZ_BUILD_INFO_JSON ${JSON.stringify(JSON.stringify({sdk:'OrzAudioCore',version:manifest.sdkVersion,abi:manifest.abiVersion,fingerprint:`orzcore-${manifest.sdkVersion}-m${manifest.schemaVersion}`}))}\n`;
const buildOutput = resolve(root, 'Sources/OrzAudioKitCXX/dispatch/build_info.generated.h');
const groups = {module:'modules',console:'retro',synth:'synth'};
const browserManifest = `globalThis.ORZ_DECODER_FORMATS=${JSON.stringify(manifest.formats.map(format => ({id:format.id,label:format.name,group:groups[format.category] ?? 'other'})))};\n`;
const browserOutput = resolve(root, 'Resources/Public/audio/decoder-manifest.generated.js');
const swiftManifest = `// Generated by script/generate-decoder-manifest.mjs. Do not edit.\npublic enum DecoderManifest {\n    public static let sdkVersion = ${quote(manifest.sdkVersion)}\n    public static let abiVersion = ${quote(manifest.abiVersion)}\n    public static let decodableFormatIDs: Set<String> = [${manifest.formats.map(format => quote(format.id)).join(', ')}]\n}\n`;
const swiftOutput = resolve(root, 'Sources/OrzAudioKit/DecoderManifest.generated.swift');
const exists = async path => access(path).then(() => true, () => false);
if (process.argv.includes('--check')) {
  if (await readFile(output, 'utf8') !== c) throw new Error('decoder manifest outputs are stale; run script/generate-decoder-manifest.mjs');
  if (await readFile(buildOutput, 'utf8') !== buildInfo) throw new Error('build info is stale; run script/generate-decoder-manifest.mjs');
  // The browser product projection belongs to OrzMusic, not the standalone
  // SDK. Validate it only when the consumer-owned output exists.
  if (await exists(browserOutput) && await readFile(browserOutput, 'utf8') !== browserManifest) throw new Error('browser manifest is stale; run script/generate-decoder-manifest.mjs');
  if (await readFile(swiftOutput, 'utf8') !== swiftManifest) throw new Error('Swift manifest is stale; run script/generate-decoder-manifest.mjs');
} else {
  await writeFile(output, c);
  await writeFile(buildOutput, buildInfo);
  if (await exists(dirname(browserOutput))) await writeFile(browserOutput, browserManifest);
  await writeFile(swiftOutput, swiftManifest);
  const web = resolve(root, 'SDK/Web/decoder-manifest.json');
  await mkdir(dirname(web), {recursive:true});
  await writeFile(web, JSON.stringify(manifest, null, 2) + '\n');
}
